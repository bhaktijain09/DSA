package loop;

public class forLoop {
    public static void main(String[] args) {
        int i;                             // variable declared
        for (i=1; i<=10; i++){            // for (initialization; condition; updation){
            System.out.println(i);       //  System.out.println(statement/ output); }
        }
    }
    
}

